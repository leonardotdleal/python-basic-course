# PRIMITIVE VARIABLES IN PYTHON #

# String
name = 'Leonardo Leal'

# Integer
age = 25

# Float
height = 1.68

# Boolean
student = True

print(name)
print(age)
print(height)
print(student)

# None (is similar to null in others languages)
working = None

print(working)

# OPERATIONS #

new_age = age + 2
name_and_city = name + ' Joinville'

print(new_age)
print(name_and_city)

height_with_cap = height + 0.023

print(height_with_cap)
